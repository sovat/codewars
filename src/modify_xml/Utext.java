package modify_xml;


public class Utext {

    public Integer textstyleno;

    public Integer parastyleno;

    public String title;

    public String tag;

    public Integer br;


    public Integer getTextstyleno() {
        return textstyleno;
    }

    public void setTextstyleno(Integer textstyleno) {
        this.textstyleno = textstyleno;
    }

    public Integer getParastyleno() {
        return parastyleno;
    }

    public void setParastyleno(Integer parastyleno) {
        this.parastyleno = parastyleno;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public Integer getBr() {
        return br;
    }

    public void setBr(Integer br) {
        this.br = br;
    }
}
